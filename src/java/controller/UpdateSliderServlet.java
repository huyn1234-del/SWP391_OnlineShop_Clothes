    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id_raw = request.getParameter("id");
        int id;
        SliderDAO s = new SliderDAO();
        try {
            id = Integer.parseInt(id_raw);
            Slider slider = s.getSliderbyid(id);
            request.setAttribute("updateSlide", slider);
            request.getRequestDispatcher("updateSlider.jsp").forward(request, response);
        } catch (NumberFormatException e) {
            
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String slider_id = request.getParameter("id");
        String tittle = request.getParameter("tittle");
        String description = request.getParameter("description");
        String image = request.getParameter("image");
        String active = request.getParameter("active");

        int id;
        SliderDAO sdb = new SliderDAO();
        try {
            id = Integer.parseInt(slider_id);
            Slider s= new Slider(id, tittle, description, image, active);
            sdb.updateSlider(s);
            response.sendRedirect("slider");
        } catch (NumberFormatException e) {
        }
    }

