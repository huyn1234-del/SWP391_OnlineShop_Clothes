package model;

/**
 *
 * @author 84983
 */
public class FeedbackChart {
    String label;
    float value;

    public FeedbackChart(String label, float value) {
        this.label = label;
        this.value = value;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public float getValue() {
        return value;
    }

    public void setValue(float value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "FeedbackChart{" + "label=" + label + ", value=" + value + '}';
    }
    
}
